
public class SelectionDemo 
{
	public static void main(String args[])
	{
		short n = 3;
		switch (n)
		{
		case 65:
			System.out.println("One");
			break;
		case 2:
			System.out.println("Two");
			break;
		case 3:
			System.out.println("Three");
			break;
		default:
			System.out.println("Inside default");
		}
	}
}
